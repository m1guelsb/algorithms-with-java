package com.m1guelsb;

public class Algorithms {
  /*
   * faster to slow
   * O(1) - Constant Time
   * - random access of a element in an array
   * - inserting at the beginning of a linked list
   * 
   * O(log n) - Logarithmic Time
   * - binary search
   * 
   * O(n) - Linear Time
   * - searching an element in an unsorted array
   * - looping through all elements in an array
   * 
   * O(n log n) - Quasilinear Time
   * - quicksort
   * - mergesort
   * - heapsort
   * 
   * O(n^2) - Quadratic Time
   * - bubble sort
   * - selection sort
   * - insertion sort
   */
}
